#include <iostream>
using namespace std;
    int main(){
       cout<<" This is about incrementing as well as decrementing "<<endl;

       int x = 20;
       cout<< ++x <<endl;
       cout<< " This was about pre incrementing "<<endl;

       int y = 40;
       cout<< y++ <<endl;
       cout<<" This is about post incrementing"<<endl;


       int a = 30;
       cout<< --a<<endl;
       cout<<" This is about pre decrementing "<<endl;

       int b = 70;
       cout<<b--<<endl;
       cout<< " This is basically about post decrementing "<<endl;
       return 0;
       
       return 0;
        
        





    }